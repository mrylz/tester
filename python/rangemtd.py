#range metodu
#bu metod ile ratgele sayıları atayabilir ve bua bağlı olarak oyunlar yapılabilir
for i in range(50,250,1):
    if(i%2==0):
        print(i)
# yukarıaki örnekte range ile 50 ile 250 sayıları arasındaki
#sayıların 1 er arttırıp çift olanları alan algoritma
rng = range(10)
sonuc = list(rng)
print(sonuc)
#yukarıdaki örnekte 10 a kadar olan sayılar bi liste içerisine atılarak işlem yapılacaktır
